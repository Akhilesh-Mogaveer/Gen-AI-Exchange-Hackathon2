import React from 'react';
import { useNavigate } from 'react-router-dom';
import Button from '../../../components/ui/Button';

const ActionButtons = ({ onBookmark, isBookmarked = false }) => {
  const navigate = useNavigate();

  const handleGenerateNew = () => {
    navigate('/career-guide');
  };

  const handleBackToDashboard = () => {
    navigate('/dashboard');
  };

  const handleBookmark = () => {
    if (onBookmark) {
      onBookmark();
    }
  };

  const handleShare = () => {
    if (navigator.share) {
      navigator.share({
        title: 'My Career Roadmap - CareerAdvisor AI',
        text: 'Check out my personalized career roadmap generated by AI!',
        url: window.location?.href
      })?.catch(console.error);
    } else {
      // Fallback to clipboard
      navigator.clipboard?.writeText(window.location?.href)?.then(() => {
        // You could add a toast notification here
        console.log('Link copied to clipboard');
      })?.catch(console.error);
    }
  };

  return (
    <div className="glass-card p-6 rounded-lg animate-fade-in">
      <div className="flex flex-col sm:flex-row gap-3">
        <Button
          variant="default"
          onClick={handleGenerateNew}
          iconName="RefreshCw"
          iconPosition="left"
          iconSize={16}
          className="flex-1 sm:flex-none"
        >
          Generate New Roadmap
        </Button>
        
        <Button
          variant="outline"
          onClick={handleBookmark}
          iconName={isBookmarked ? "BookmarkCheck" : "Bookmark"}
          iconPosition="left"
          iconSize={16}
          className="flex-1 sm:flex-none"
        >
          {isBookmarked ? 'Bookmarked' : 'Bookmark Results'}
        </Button>
        
        <Button
          variant="ghost"
          onClick={handleShare}
          iconName="Share2"
          iconPosition="left"
          iconSize={16}
          className="flex-1 sm:flex-none"
        >
          Share
        </Button>
        
        <Button
          variant="secondary"
          onClick={handleBackToDashboard}
          iconName="ArrowLeft"
          iconPosition="left"
          iconSize={16}
          className="flex-1 sm:flex-none"
        >
          Back to Dashboard
        </Button>
      </div>
      <div className="mt-4 p-3 rounded-lg bg-muted/20 border border-border/10">
        <p className="text-sm text-muted-foreground text-center">
          Your roadmap was generated on {new Date()?.toLocaleDateString('en-US', { 
            year: 'numeric', 
            month: 'long', 
            day: 'numeric' 
          })}
        </p>
      </div>
    </div>
  );
};

export default ActionButtons;